package main;

public class EDTInstrument {

	private String clearingContractCode;
	private String expiryDate;
	
	
	public String getClearingContractCode() {
		return clearingContractCode;
	}
	public void setClearingContractCode(String clearingContractCode) {
		this.clearingContractCode = clearingContractCode;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	
	
	
}
